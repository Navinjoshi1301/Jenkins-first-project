public class Array {
    public static void main(String[] args) {
        int arr[]={5,6,8,6,89,5,4};
        for(int i=0;i<arr.length;i++){
            System.out.println(arr[i]+" ");
            System.out.println(arr[i]+"  ");
        }
        System.out.println(arr);
        System.out.println("");
    }
    
}
